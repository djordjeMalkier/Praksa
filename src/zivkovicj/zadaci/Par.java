package zivkovicj.zadaci;

public class Par {
    double x;
    double y;
    public Par(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "Par{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
